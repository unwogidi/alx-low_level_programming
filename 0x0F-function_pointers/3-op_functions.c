#include "3-calc.h"

/**
 * op_add - adds two integers
 * @a: integer a
 * @b: integer b
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two integers
 * @a: integer a
 * @b: integer b
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: integer a
 * @b: integer b
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides integer a by integer b
 * @a: integer a
 * @b: integer b
 * Return: a \ b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of a divided by b
 * @a: integer a
 * @b: integer b
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
